package AtividadesJava;
/*
   Demostração  do laço for.
 */
public class Ex6 {
    public static void main(String[] args) {
        int count;

        for (count = 0; count < 5; count++) // Esse laço repetirá 5 vezes
            System.out.println("Contagem: " + count);

            System.out.println("Fim");
    }
}
