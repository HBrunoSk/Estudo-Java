package AtividadesJava;
/*
Esse codigo demonstra uma variavel
 */
public class Ex2 {
    public static void main(String[] args) {
        int var1; // dando nome a variavel "var1"
        int var2; // Dando nome a variavel "var2"
        int soma; // variavel soma, pra amarzenar o resultado entre " Var1 e Var2 "

        var1 = 1024; // essa instrução é pra dar valor na variavel "var1"
        var2 = 140; // dando valor a variavel "var2"

        soma = var1 + var2;// somando Var1 e Var2 e armazenando o resultado na variavel soma

        System.out.println(var1 + " + " + var2 + " = " + soma);// imprimindo o resultado
    }
}
